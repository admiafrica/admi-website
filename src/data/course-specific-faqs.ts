// Helper function to get FAQs for a specific course
export function getCourseSpecificFAQs(courseSlug: string, programType: 'certificate' | 'diploma'): any[] {
  const slug = courseSlug.toLowerCase()

  if (programType === 'certificate') {
    if (slug.includes('digital-marketing')) return DIGITAL_MARKETING_CERTIFICATE_DETAILED_FAQS
    if (slug.includes('graphic-design')) return GRAPHIC_DESIGN_CERTIFICATE_DETAILED_FAQS
    if (slug.includes('video-production')) return VIDEO_PRODUCTION_CERTIFICATE_DETAILED_FAQS
    if (slug.includes('photography')) return PHOTOGRAPHY_CERTIFICATE_DETAILED_FAQS
    if (slug.includes('music-production')) return MUSIC_PRODUCTION_CERTIFICATE_DETAILED_FAQS
  } else {
    if (slug.includes('film') || slug.includes('television')) return FILM_TV_DIPLOMA_DETAILED_FAQS
    if (slug.includes('animation') || slug.includes('motion')) return ANIMATION_DIPLOMA_DETAILED_FAQS
    if (slug.includes('graphic-design')) return GRAPHIC_DESIGN_DIPLOMA_DETAILED_FAQS
    if (slug.includes('music-production')) return MUSIC_PRODUCTION_DIPLOMA_DETAILED_FAQS
    if (slug.includes('sound-engineering') || slug.includes('audio')) return SOUND_ENGINEERING_DIPLOMA_DETAILED_FAQS
  }

  return []
}
